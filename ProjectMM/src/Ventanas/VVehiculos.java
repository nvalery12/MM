    private void Btn_ActuaizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_ActuaizarActionPerformed
        String formatoFecha = ("yyyy/MM/dd");
        SimpleDateFormat formateo = new SimpleDateFormat(formatoFecha);
        
        if (!(this.TF_CodVeh.getText().equals("")) && !(this.TF_PlacVeh.getText().equals("")) && !(this.TF_AceiteUt.getText().equals("")) && !(this.RS_FechaAdq.getDatoFecha().toString().isEmpty()) && !(this.TF_MarcaVeh.getText().equals("") && !(this.TF_ModVeh.getText().equals(""))) ) {

                java.util.Date fechaDate = this.RS_FechaAdq.getDatoFecha();
                String FechaAdq = formateo.format(fechaDate);
            
            
                String CodVeh = this.TF_CodVeh.getText();
                String CICliente = this.TF_CICliente.getText();
                String PlacaVeh = this.TF_PlacVeh.getText(); 
                String AceiteUt = this.TF_AceiteUt.getText();
                String MarcaVeh = this.TF_MarcaVeh.getText();
                String ModeloVeh = this.TF_ModVeh.getText();
               

                veh.actualizarVehiculos(CodVeh,CICliente,PlacaVeh, AceiteUt,FechaAdq,MarcaVeh,ModeloVeh);

            }else {
                JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_Btn_ActuaizarActionPerformed

    private void Btn_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_BuscarActionPerformed
        String formatoFecha = ("yyyy/MM/dd");
        SimpleDateFormat formateo = new SimpleDateFormat(formatoFecha);
        
        if (!(this.TF_CodVeh.getText().equals(""))) {
            java.util.Date fechaDate = this.RS_FechaAdq.getDatoFecha();
            String FechaAdq= formateo.format(fechaDate);            
            
            String[] datosAr = veh.buscarVehiculos(TF_CodVeh.getText());
            
            this.TF_PlacVeh.setText(datosAr[0]);
            this.TF_AceiteUt.setText(datosAr[1]);
            this.RS_FechaAdq.setDatoFecha(this.veh.FechaAdq(FechaAdq));
            this.TF_MarcaVeh.setText(datosAr[2]);
            this.TF_ModVeh.setText(datosAr[3]);
            this.TF_CICliente.setText(datosAr[4]);

        }else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
        this.TF_CodVeh.setEnabled(false);
    }//GEN-LAST:event_Btn_BuscarActionPerformed

