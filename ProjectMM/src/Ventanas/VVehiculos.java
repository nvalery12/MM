    private void Btn_Buscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_Buscar1ActionPerformed
         if (!(this.TF_CICliente.getText().equals(""))) {
            String NombreCt = this.veh.buscarCliente(Integer.parseInt(this.TF_CICliente.getText()));
            if ((NombreCt.equals(""))) {
                JOptionPane.showMessageDialog(null, "Cliente no encontrado. Por favor, ingrese una cedula registrada o registre un nuevo vehiculo", "Advertencia", JOptionPane.WARNING_MESSAGE);
                this.TF_CICliente.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Cliente: "+NombreCt+" encontrado","Busqueda exitosa", JOptionPane.INFORMATION_MESSAGE);
                this.TF_CICliente.setEnabled(false);
                this.Btn_Nuevo.setEnabled(true);
            }
        }
    }//GEN-LAST:event_Btn_Buscar1ActionPerformed

