    private void Btn_NuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_NuevoActionPerformed
        if (!(this.TF_CiEmp.getText().equals("")) && !(this.TF_NombreEmp.getText().equals("")) && !(this.TF_Direccion.getText().equals("")) && !(this.TF_Sueldo.getText().equals("")) && !(this.TF_Telefono.getText().equals("") && !(this.TF_RifAg.getText().equals("")))) {

            String CIEmp = this.TF_CiEmp.getText();
            String RifAg = this.TF_RifAg.getText();
            String NombreEmp = this.TF_NombreEmp.getText();
            String Direccion = this.TF_Direccion.getText();
            float Sueldo = Float.parseFloat(this.TF_Sueldo.getText());
            String Telefono = this.TF_Telefono.getText();
            

            per.nuevoPersonal(CIEmp,RifAg,NombreEmp, Direccion,Sueldo,Telefono);

        }else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_Btn_NuevoActionPerformed

    private void Btn_ActuaizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_ActuaizarActionPerformed
        if (!(this.TF_CiEmp.getText().equals("")) && !(this.TF_NombreEmp.getText().equals("")) && !(this.TF_Direccion.getText().equals("")) && !(this.TF_Sueldo.getText().equals("")) && !(this.TF_Telefono.getText().equals("") && !(this.TF_RifAg.getText().equals(""))) ) {

            String CedulaCt = this.TF_CiEmp.getText();
            String RifAg = this.TF_RifAg.getText();
            String NombreCt = this.TF_NombreEmp.getText();
            String EmailCt = this.TF_Direccion.getText();
            float Sueldo = Float.parseFloat(this.TF_Sueldo.getText());
            String Telefono = this.TF_Telefono.getText();
            
            
            per.actualizarPersonal(CedulaCt,RifAg,NombreCt, EmailCt,Sueldo,Telefono);

        }else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_Btn_ActuaizarActionPerformed

