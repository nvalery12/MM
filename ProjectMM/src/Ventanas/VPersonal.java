    private void Btn_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_BuscarActionPerformed
        if (!(this.TF_CiEmp.getText().equals(""))) {

            String[] datosAr = per.buscarPersonal(TF_CiEmp.getText());

            this.TF_NombreEmp.setText(datosAr[0]);
            this.TF_Direccion.setText(datosAr[1]);
            this.TF_Sueldo.setText(datosAr[2]);
            this.TF_Telefono.setText(datosAr[3]);
            this.TF_RifAg.setText(datosAr[4]);

        }else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
        this.TF_CiEmp.setEnabled(false);
    }//GEN-LAST:event_Btn_BuscarActionPerformed

    private void Btn_Buscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_Buscar1ActionPerformed
        if (!(this.TF_RifAg.getText().equals(""))) {
            String nombencargado = this.per.buscarAgencia(Integer.parseInt(this.TF_RifAg.getText()));
            if ((nombencargado.equals(""))) {
                JOptionPane.showMessageDialog(null, "Agencia no encontrada. Por favor, ingrese un RIF registrado o registre un nuevo empleado", "Advertencia", JOptionPane.WARNING_MESSAGE);
                this.TF_RifAg.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Empleado: "+nombencargado+" encontrado","Busqueda exitosa", JOptionPane.INFORMATION_MESSAGE);
                this.TF_RifAg.setEnabled(false);
                this.Btn_Nuevo.setEnabled(true);
            }
        }
    }//GEN-LAST:event_Btn_Buscar1ActionPerformed

