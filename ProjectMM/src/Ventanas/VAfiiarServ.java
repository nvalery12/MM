    private void Btn_NuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_NuevoActionPerformed
        if (!(this.TF_Agencia.getText().equals("")) && !(this.TF_Capacidad.getText().equals("")) && !(this.TF_Servicio.getText().equals(""))) {

            String Serv = this.TF_Servicio.getText();
            String Agen = this.TF_Agencia.getText();
            int Capac = Integer.parseInt(this.TF_Capacidad.getText());

            this.datos.nuevoServAg(Serv, Agen, Capac);
        } else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_Btn_NuevoActionPerformed

    private void Btn_ActuaizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_ActuaizarActionPerformed
        if (!(this.TF_Agencia.getText().equals("")) && !(this.TF_Capacidad.getText().equals("")) && !(this.TF_Servicio.getText().equals(""))) {

            String Serv = this.TF_Servicio.getText();
            String Agen = this.TF_Agencia.getText();
            int Capac = Integer.parseInt(this.TF_Capacidad.getText());

            this.datos.nuevoServAg(Serv, Agen, Capac);
        } else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_Btn_ActuaizarActionPerformed

    private void Btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_EliminarActionPerformed
         if (!(this.TF_Agencia.getText().equals("")) && !(this.TF_Servicio.getText().equals(""))) {

            String Serv = this.TF_Agencia.getText();

            datos.eliminarServAg(Serv, this.TF_Agencia.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_Btn_EliminarActionPerformed

