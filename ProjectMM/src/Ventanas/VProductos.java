    private void Btn_ActuaizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_ActuaizarActionPerformed
        if (!(this.CodPrText.getText().equals("")) && !(this.MinPrText.getText().equals("")) && !(this.PrecioPrText.getText().equals("")) && !(this.EcoPrText.getText().equals(""))
                && !(this.NombPrText.getText().equals("")) && !(this.MaxPrText.getText().equals("")) && !(this.CodLSText.getText().equals(""))
                && !(this.DescPrText.getText().equals("")) && !(this.FabriPrText.getText().equals("")) && !(this.CantText.getText().equals(""))) {
            
            try {
                int max = Integer.parseInt(this.MaxPrText.getText());
                int min = Integer.parseInt(this.MinPrText.getText());
                int cant = Integer.parseInt(this.CantText.getText());
                
                db.updateProducto(this.CodPrText.getText(), this.NombPrText.getText(), this.CodLSText.getText(), max, min, this.PrecioPrText.getText(), 
                        this.EcoPrText.getText(), this.DescPrText.getText(), this.FabriPrText.getText(), cant);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: Maximo,Minimo y Cantidad deben ser numeros", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }

        }else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_Btn_ActuaizarActionPerformed

    private void Btn_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_BuscarActionPerformed
        if (!(this.CodPrText.getText().equals(""))) {
            
            String[] datoAr = db.obtenerProducto(this.CodPrText.getText());
            
            this.NombPrText.setText(datoAr[0]);
            this.CodLSText.setText(datoAr[1]);
            this.MaxPrText.setText(datoAr[2]);
            this.MinPrText.setText(datoAr[3]);
            this.PrecioPrText.setText(datoAr[4]);
            this.EcoPrText.setText(datoAr[5]);
            this.DescPrText.setText(datoAr[6]);
            this.FabriPrText.setText(datoAr[7]);
            this.CantText.setText(datoAr[8]);
   
        }else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos faltantes", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
        if (!(this.MinPrText.getText().equals("")) && !(this.PrecioPrText.getText().equals("")) && !(this.EcoPrText.getText().equals(""))) {
            this.CodPrText.setEnabled(false);
        }
        else {
            JOptionPane.showMessageDialog(null, "Registro no encontrado","Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_Btn_BuscarActionPerformed

